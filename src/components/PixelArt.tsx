interface FactionIconProps {
  faction: FactionIconId;
  size?: number;
  className?: string;
}

export const FactionIcon: React.FC<FactionIconProps> = ({
  faction,
  size = 32,
  className = ''
}) => {
  const icon = FACTION_ICONS[faction];
  if (!icon) return null;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 16 16"
      className={`faction-icon ${className}`}
      style={{ imageRendering: 'pixelated' }}
    >
      {icon.map((row, y) =>
        row.map((colorIndex, x) => {
          if (colorIndex === 0) return null;
          return (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width={1}
              height={1}
              fill={PALETTE[colorIndex]}
            />
          );
        })
      )}
    </svg>
  );
};

